int	ft_sqrt(int nb)
{
	int	res;

	res = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (res);
	while (res <= nb / res)
	{
		if (res * res == nb)
			return (res);
		res++;
	}
	return (0);
}
