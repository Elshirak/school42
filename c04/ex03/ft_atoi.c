int	ft_atoi(char *str)
{
	int		result;
	int		sign;

	result = 0;
	sign = 1;
	while ((!(*str >= 48 && *str <= 57)) && *str != '\0')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while ((*str >= '0' && *str <= '9') && *str != '\0')
	{
		result = result * 10 + *str - '0';
		++str;
	}
	return (sign * result);
}
